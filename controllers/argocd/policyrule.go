func PolicyRuleForRedisHa(cr *argoprojv1alpha1.ArgoCD) []v1.PolicyRule {
	rules := []v1.PolicyRule{
		{
			APIGroups: []string{
				"",
			},
			Resources: []string{
				"endpoints",
			},
			Verbs: []string{
				"get",
			},
		},
	}

	if err := ApplyReconcilerHook(cr, &rules, "policyRuleForRedisHa"); err != nil {
		logr.Error(err, "error from reconcile hook")
	}

	return rules
}

